#5. Tienda de mascotas

animal = input("Ingrese mascota (perro, gato, conejo): ").lower()

if animal == "perro":
    print("Recomendado: concentrado para perro")
elif animal == "gato":
    print("Recomendado: concentrado para gato")
elif animal == "conejo":
    print("Recomendado: verduras y heno")
